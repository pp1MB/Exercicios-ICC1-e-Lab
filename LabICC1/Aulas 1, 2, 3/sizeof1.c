#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("Tam char: %d\n", sizeof(char));
    printf("Tam int: %d\n", sizeof(int));
    printf("Tam float: %d\n", sizeof(float));
    printf("Tam double: %d\n", sizeof(double));
    printf("Tam unsigned char: %d\n", sizeof(unsigned char));
    printf("Tam signed char: %d\n", sizeof(signed char));
    printf("Tam unsigned int: %d\n", sizeof(unsigned int));
    printf("Tam signed int: %d\n", sizeof(signed int));
    printf("Tam short int: %d\n", sizeof(short int));
    printf("Tam long int: %d\n", sizeof(long int));
    printf("Tam long long int: %d\n", sizeof(long long int));
    return 0;
}
